package themoviez.newsletter.entity;

public class NewsletterDTO {

	private int news_num;
	private String news_title;
	private String m_id;
	private String news_content;
	private String news_writeday;
	private int news_readnum;
	private String news_ip;
	
	public int getNews_num() {
		return news_num;
	}
	public void setNews_num(int news_num) {
		this.news_num = news_num;
	}
	public String getNews_title() {
		return news_title;
	}
	public void setNews_title(String news_title) {
		this.news_title = news_title;
	}
	public String getM_id() {
		return m_id;
	}
	public void setM_id(String m_id) {
		this.m_id = m_id;
	}
	public String getNews_content() {
		return news_content;
	}
	public void setNews_content(String news_content) {
		this.news_content = news_content;
	}
	public String getNews_writeday() {
		return news_writeday;
	}
	public void setNews_writeday(String news_writeday) {
		this.news_writeday = news_writeday;
	}
	public int getNews_readnum() {
		return news_readnum;
	}
	public void setNews_readnum(int news_readnum) {
		this.news_readnum = news_readnum;
	}
	public String getNews_ip() {
		return news_ip;
	}
	public void setNews_ip(String news_ip) {
		this.news_ip = news_ip;
	}
	
	
}
